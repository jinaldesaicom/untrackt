// ── Helper Functions ──────────────────────────

export function getResourcesByCategory(category) {
  if (category === 'all') return systemDesignResources
  return systemDesignResources.filter((r) => r.category === category)
}

export function getPopularResources() {
  return systemDesignResources.filter((r) => r.isPopular)
}

export function getTotalCount() {
  return systemDesignResources.length
}

export function getLevelCounts(resources = systemDesignResources) {
  const counts = { beginner: 0, intermediate: 0, advanced: 0, free: 0 }
  resources.forEach((r) => {
    if (counts[r.level] !== undefined) counts[r.level]++
    if (r.pricing === 'free') counts.free++
  })
  return counts
}

export function getCategoryCounts() {
  const counts = { all: systemDesignResources.length }
  systemDesignResources.forEach((r) => {
    counts[r.category] = (counts[r.category] || 0) + 1
  })
  return counts
}
