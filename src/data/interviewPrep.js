// ── Helper Functions ──────────────────────────────

export function getResourcesByCategory(category) {
  if (category === 'all') return interviewPrepResources
  return interviewPrepResources.filter((r) => r.category === category)
}

export function getPopularResources() {
  return interviewPrepResources.filter((r) => r.isPopular)
}

export function getCategoryCounts() {
  const counts = { all: interviewPrepResources.length }
  interviewPrepResources.forEach((r) => {
    counts[r.category] = (counts[r.category] || 0) + 1
  })
  return counts
}

export function getLevelCounts() {
  const counts = { beginner: 0, intermediate: 0, advanced: 0, free: 0 }
  interviewPrepResources.forEach((r) => {
    counts[r.level] = (counts[r.level] || 0) + 1
    if (r.pricing === 'free') counts.free++
  })
  return counts
}

export function getTotalCount() {
  return interviewPrepResources.length
}
