export function getServicesByCategory(category) {
  if (category === 'all') return statusPages
  return statusPages.filter((s) => s.category === category)
}

export function getPopularServices() {
  return statusPages.filter((s) => s.isPopular)
}

export function getTotalCount() {
  return statusPages.length
}

export function getServiceById(id) {
  return statusPages.find((s) => s.id === id)
}

export function getCategoryCounts() {
  const counts = { all: statusPages.length }
  for (const s of statusPages) {
    counts[s.category] = (counts[s.category] || 0) + 1
  }
  return counts
}
