// ── Helper functions ────────────────────────────────────────

export function getReposByCategory(category) {
  if (category === 'all') return githubStars
  return githubStars.filter((r) => r.category === category)
}

export function getPopularRepos() {
  return githubStars.filter((r) => r.isPopular)
}

export function getRepoById(id) {
  return githubStars.find((r) => r.id === id) || null
}

export function getCategoryCounts() {
  const counts = { all: githubStars.length }
  for (const cat of githubStarsCategories) {
    if (cat.id !== 'all') {
      counts[cat.id] = githubStars.filter((r) => r.category === cat.id).length
    }
  }
  return counts
}

export function getTotalCount() {
  return githubStars.length
}
