// ── Helper Functions ────────────────────────

export function getCertsByCategory(category) {
  if (category === 'all') return certifications
  return certifications.filter((c) => c.category === category)
}

export function getCertsByLevel(level) {
  if (level === 'all') return certifications
  return certifications.filter((c) => c.level === level)
}

export function getPopularCerts() {
  return certifications.filter((c) => c.isPopular)
}

export function getCertById(id) {
  return certifications.find((c) => c.id === id)
}

export function getTotalCertCount() {
  return certifications.length
}

export function getCategoryCounts() {
  const counts = {}
  for (const c of certifications) {
    counts[c.category] = (counts[c.category] || 0) + 1
  }
  counts.all = certifications.length
  return counts
}

export function getLevelCounts(certs = certifications) {
  const counts = { all: certs.length }
  for (const c of certs) {
    counts[c.level] = (counts[c.level] || 0) + 1
  }
  return counts
}

export function getPrerequisites(certId) {
  const cert = getCertById(certId)
  if (!cert || !cert.prerequisites.length) return []
  return cert.prerequisites.map(getCertById).filter(Boolean)
}
